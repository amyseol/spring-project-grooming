package kr.co.gudi.photo.dto;

public class photoDTO {
	
	
	private int photo_num ;
	private String photo_code ;
	private int photo_n ;
	private String oriFileName ;
	private String newFileName;
	
	public int getPhoto_num() {
		return photo_num;
	}
	public void setPhoto_num(int photo_num) {
		this.photo_num = photo_num;
	}
	public String getPhoto_code() {
		return photo_code;
	}
	public void setPhoto_code(String photo_code) {
		this.photo_code = photo_code;
	}
	public int getPhoto_n() {
		return photo_n;
	}
	public void setPhoto_n(int photo_n) {
		this.photo_n = photo_n;
	}
	public String getOriFileName() {
		return oriFileName;
	}
	public void setOriFileName(String oriFileName) {
		this.oriFileName = oriFileName;
	}
	public String getNewFileName() {
		return newFileName;
	}
	public void setNewFileName(String newFileName) {
		this.newFileName = newFileName;
	}
	
	
	

}
